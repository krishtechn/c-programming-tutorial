#include<stdio.h>
int main(){
	
//	declaring variable
	int a = 10;
	float b = 1.0;
	char ch = 'A';
	double d = 15000;
	
//	printing
printf("the integer value is : %d\n", a);

printf("the float value is : %f\n", b);

printf("the char value is : %c\n", ch);

printf("the double is : %lg\n", d);	
	
	return 0;
}


